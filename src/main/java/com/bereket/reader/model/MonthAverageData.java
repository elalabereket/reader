package com.bereket.reader.model;

public class MonthAverageData {

	private String month;
	
	private double averageOpenPrice;
	
	private double averageClosePrice;

	public String getMonth() {
		return month;
	}

	public void setMonth(String month) {
		this.month = month;
	}

	public double getAverageOpenPrice() {
		return averageOpenPrice;
	}

	public void setAverageOpenPrice(double averageOpenPrice) {
		this.averageOpenPrice = averageOpenPrice;
	}

	public double getAverageClosePrice() {
		return averageClosePrice;
	}

	public void setAverageClosePrice(double averageClosePrice) {
		this.averageClosePrice = averageClosePrice;
	}
	
	
}
